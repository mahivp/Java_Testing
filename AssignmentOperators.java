public class AssignmentOperators {
    public static void main(String[] args) {

        int a=10, b=7, c=9;

        // assigment operators

        System.out.println( "+=: " + (a+=5));
        System.out.println("-=: " + (a-=5));
        System.out.println("*=: " + (b*=5));
        System.out.println("/=: " + (a/=5));
        System.out.println("%=: " + (b%=5));
        System.out.println("aaa: " + a);
        System.out.println("%=: " + (c%=5));

    }
}
